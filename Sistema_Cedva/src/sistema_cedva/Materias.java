    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        
        if (txtClMateria.getText().equals("") && txtNbMateria.getText().equals(""))
            {    JOptionPane.showMessageDialog(rootPane, "Debes agregar una clave y una materia", "MENSAJE DE ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            }
        else if (txtClMateria.getText().equals(""))
            {    JOptionPane.showMessageDialog(rootPane, "Debes agregar una clave para la materia", "MENSAJE DE ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            }
        else if (txtNbMateria.getText().equals(""))
            {    JOptionPane.showMessageDialog(rootPane, "Debes agregar una materia", "MENSAJE DE ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            }
        else if(!txtClMateria.getText().equals("") && !txtNbMateria.getText().equals("")){  
        try {
            // agregar
            // the mysql insert statement
            MySqlConnect mysqlConnect = new MySqlConnect();
            String query="";
            String message="";
            if(ID_MATERIA==0)
            {
                message="Se agrego una materia";
                query = " insert into c_materia (CL_MATERIA, NB_MATERIA)"
                    + " values (?, ?)";
                
            // create the mysql insert preparedstatement
            PreparedStatement preparedStmt = mysqlConnect.connect().prepareStatement(query);
            preparedStmt.setString (1, txtClMateria.getText());
            preparedStmt.setString (2, txtNbMateria.getText());
            // execute the preparedstatement
            preparedStmt.execute();
            clear();
            }
            else 
            {
                message="Se edito la materia seleccionada";

                query = "update c_materia set NB_MATERIA = ? where ID_MATERIA = ?";
                PreparedStatement preparedStmt = mysqlConnect.connect().prepareStatement(query);
                preparedStmt.setString(1, txtNbMateria.getText());             
                preparedStmt.setInt(2, ID_MATERIA);
                 // execute the java preparedstatement
                 preparedStmt.executeUpdate();
                 clear();
                 txtClMateria.enable(true);
            }
            loadData();
            Icon image= new ImageIcon(getClass().getResource("/imagenes/correcto_sin_f.png"));
            JOptionPane.showMessageDialog(rootPane, message,"",JOptionPane.INFORMATION_MESSAGE,image);
            mysqlConnect.connect().close();
        } catch (SQLException ex) {
            Logger.getLogger(Materias.class.getName()).log(Level.SEVERE, null, ex);
        }
          }
         else 
        {
           
        }
         
    }//GEN-LAST:event_btn_agregarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(ID_MATERIA!=0)
        {
            for (E_MATERIAS mat : materias) {
                if(mat.getID_MATERIA()==ID_MATERIA)
                {
                    txtClMateria.enable(false);
                    txtClMateria.setText(mat.getCL_MATERIA());                    
                    txtNbMateria.setText(mat.getNB_MATERIA());
                    break;
                }
            }
        }
        else 
        {
        Icon image= new ImageIcon(getClass().getResource("/imagenes/selecciona_sin_f.png"));
        JOptionPane.showMessageDialog(null, "Selecciona una materia","",JOptionPane.INFORMATION_MESSAGE,image);
        }
   
    }//GEN-LAST:event_btnEditarActionPerformed

